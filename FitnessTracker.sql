CREATE TABLE VIEWAPPOINTMENT(
	SERIAL_NO INT(100) PRIMARY KEY,
    FULLNAME VARCHAR(30) NOT NULL,
    PHONE_NUMBER VARCHAR(30) NOT NULL,
    EMAIL VARCHAR(150),
    AGE INT(5),
    ADDRESS VARCHAR(100) NOT NULL,
    TRAINER_PREFERENCE ENUM('MALE', 'FEMALE', 'NULL'),
    PHYSIO_REQUIRED ENUM('YES', 'NO') NOT NULL, 
    PACKAGE INT(10),
    FOREIGN KEY(FULL_NAME) REFERENCES POSTAPPOINTMENT(FULL_NAME),
	FOREIGN KEY(PHONE_NUMBER) REFERENCES POSTAPPOINTMENT(PHONE_NUMBER),
	FOREIGN KEY(EMAIL) REFERENCES POSTAPPOINTMENT(EMAIL), 
	FOREIGN KEY(AGE) REFERENCES POSTAPPOINTMENT(AGE),
	FOREIGN KEY(ADDRESS) REFERENCES POSTAPPOINTMENT(ADDRESS_LINE1),
	FOREIGN KEY(FULL_NAME) REFERENCES POSTAPPOINTMENT(ADDRESS_LINE2),
	FOREIGN KEY(TRAINER_PREFERENCE) REFERENCES POSTAPPOINTMENT(TRAINER_PREFERENCE),
    FOREIGN KEY(PHYSIO_REQUIRED) REFERENCES POSTAPPOINTMENT(PHYSIO_REQUIRED),
    FOREIGN KEY(PACKAGE) REFERENCES POSTAPPOINTMENT(PACKAGE)
);

