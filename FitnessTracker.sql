CREATE TABLE POSTAPPOINTMENT(
	FULLNAME VARCHAR(30) NOT NULL,
    PHONE_NUMBER VARCHAR(30) NOT NULL,
    EMAIL VARCHAR(150),
	AGE INT(5),
    ADDRESS_LINE1 VARCHAR(100) NOT NULL,
    ADDRESS_LINE2 VARCHAR(100) NOT NULL,
    CITY VARCHAR(20),
    STATE VARCHAR(20), 
    COUNTRY VARCHAR(20), 
    PINCODE INT(20),
    TRAINER_PREFERENCE ENUM('MALE', 'FEMALE', 'NULL'),
    PHYSIO_REQUIRED ENUM('YES', 'NO') NOT NULL, 
    PACKAGE ENUM('ONE-TIME', 'FOUR PER WEEK', 'FIVE PER WEEK'),
    DURATION_IN_WEEKS INT(10),
	TOTAL_AMOUNT INT(30)
);
